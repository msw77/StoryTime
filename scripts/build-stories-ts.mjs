/**
 * Convert generatedStories.json into stories.ts
 * Run with: node scripts/build-stories-ts.mjs
 */

import { readFileSync, writeFileSync } from "fs";
import { fileURLToPath } from "url";
import { dirname, join } from "path";

const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);

const data = JSON.parse(readFileSync(join(__dirname, "..", "src", "data", "generatedStories.json"), "utf-8"));

const GENRE_COLORS = {
  adventure: "#ef4444",
  fantasy: "#a855f7",
  friendship: "#f59e0b",
  silly: "#22c55e",
  mystery: "#6366f1",
  science: "#06b6d4",
  animals: "#f97316",
  sports: "#14b8a6",
};

// Group stories by genre for nice ordering
const genreOrder = ["adventure", "fantasy", "friendship", "silly", "mystery", "science", "animals", "sports"];

let output = `import { Story } from "@/types/story";

// Auto-generated built-in story library
// Generated by scripts/generate-builtin-stories.mjs

export const ALL_STORIES: Story[] = [\n`;

for (const genre of genreOrder) {
  const genreStories = Object.entries(data).filter(([, s]) => s.genre === genre);
  if (genreStories.length === 0) continue;

  output += `  // ── ${genre.charAt(0).toUpperCase() + genre.slice(1)} ──\n`;

  for (const [, story] of genreStories) {
    const pages = story.pages.map(p => `["${p.label}", ${JSON.stringify(p.text)}]`).join(", ");
    const fullPages = story.pages.map(p => `{ scene: ${JSON.stringify(p.scene)}, mood: ${JSON.stringify(p.mood)}, sounds: ${JSON.stringify(p.sounds || [])} }`).join(", ");
    const color = GENRE_COLORS[story.genre] || "#6366f1";

    output += `  { id: ${JSON.stringify(story.id)}, title: ${JSON.stringify(story.title)}, emoji: ${JSON.stringify(story.emoji)}, color: "${color}", genre: ${JSON.stringify(story.genre)}, age: ${JSON.stringify(story.age)}, pages: [${pages}], fullPages: [${fullPages}] },\n`;
  }
  output += "\n";
}

output += "];\n";

const outPath = join(__dirname, "..", "src", "data", "stories.ts");
writeFileSync(outPath, output);
console.log(`Written to ${outPath}`);
console.log(`${Object.keys(data).length} stories`);
