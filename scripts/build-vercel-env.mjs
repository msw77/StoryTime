/**
 * Generate a Vercel-ready env import file from .env.local.
 *
 * Vercel's "Environment Variables" screen supports bulk paste: a block
 * of KEY=value lines imported in one shot. This script produces exactly
 * that, with three safety measures on top of a plain copy:
 *
 *   1. STRIPS NEXT_PUBLIC_DEV_AUTH_BYPASS — that flag is dev-only and
 *      we explicitly do not want it ever landing in a Vercel env.
 *   2. ADDS a placeholder for ADMIN_EMAILS if missing — the admin
 *      cost-dashboard gate uses it, and forgetting this in prod would
 *      lock you out of /admin/costs.
 *   3. LEAVES feedback vars commented out by default — they're beta-
 *      only and you'll set one after the first deploy succeeds.
 *
 * Output goes to vercel-env-import.txt (gitignored). Open it, copy
 * all, paste into Vercel's bulk-import field, then delete the file.
 */

import { readFileSync, writeFileSync, existsSync, appendFileSync } from "fs";
import { fileURLToPath } from "url";
import { dirname, join } from "path";

const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);
const ROOT = join(__dirname, "..");

const src = readFileSync(join(ROOT, ".env.local"), "utf-8");

// Vars to EXCLUDE — never ship these to Vercel.
const STRIP = new Set([
  "NEXT_PUBLIC_DEV_AUTH_BYPASS",
]);

// Parse lines, preserving structure but filtering.
const out = [];
out.push("# StoryTime — Vercel environment variables");
out.push("# Generated from .env.local by scripts/build-vercel-env.mjs");
out.push("# Paste this entire block into Vercel → Settings → Environment Variables");
out.push("# → 'Add Another' dropdown → 'Import .env File' OR paste directly.");
out.push("");

const seen = new Set();
for (const rawLine of src.split(/\r?\n/)) {
  const line = rawLine.trim();
  if (!line) continue;
  if (line.startsWith("#")) {
    out.push(line);
    continue;
  }
  const eq = line.indexOf("=");
  if (eq < 0) continue;
  const key = line.slice(0, eq).trim();
  const val = line.slice(eq + 1).trim();
  if (STRIP.has(key)) {
    out.push(`# ${key} — INTENTIONALLY OMITTED (dev-only flag).`);
    continue;
  }
  out.push(`${key}=${val}`);
  seen.add(key);
}

// Make sure admin access is set so /admin/costs isn't locked out.
if (!seen.has("ADMIN_EMAILS")) {
  out.push("");
  out.push("# Admin dashboard access. Comma-separated emails that can open /admin/costs.");
  out.push("# REPLACE with your real email before pasting to Vercel.");
  out.push("ADMIN_EMAILS=your-email@example.com");
}

// Beta feedback vars — commented out. User uncomments ONE after
// first deploy works.
out.push("");
out.push("# ── Beta feedback (OPTIONAL — set ONE of these after first deploy) ──");
out.push("# For a quick email-based feedback link:");
out.push("# NEXT_PUBLIC_FEEDBACK_EMAIL=you@yourdomain.com");
out.push("# For a Tally / Typeform / Google Forms link:");
out.push("# NEXT_PUBLIC_FEEDBACK_URL=https://tally.so/r/XXXXXX");

const outPath = join(ROOT, "vercel-env-import.txt");
writeFileSync(outPath, out.join("\n") + "\n");

// Ensure the file is gitignored so it doesn't end up in the repo.
const gitignorePath = join(ROOT, ".gitignore");
if (existsSync(gitignorePath)) {
  const ig = readFileSync(gitignorePath, "utf-8");
  if (!ig.includes("vercel-env-import.txt")) {
    appendFileSync(gitignorePath, "\n# Generated by scripts/build-vercel-env.mjs — delete after use.\nvercel-env-import.txt\n");
  }
}

console.log(`✓ Wrote ${outPath}`);
console.log(`  (${seen.size + (seen.has("ADMIN_EMAILS") ? 0 : 1)} variables)`);
console.log(`  Added to .gitignore.`);
console.log(``);
console.log(`Next:`);
console.log(`  1. Open vercel-env-import.txt in a text editor`);
console.log(`  2. Replace ADMIN_EMAILS=your-email@example.com with your real email`);
console.log(`  3. Copy ALL contents`);
console.log(`  4. Vercel → your project → Settings → Environment Variables`);
console.log(`     → click "Import .env File" (or paste into the bulk field)`);
console.log(`  5. Delete vercel-env-import.txt after you're done`);
