export type VoiceMode = "ai" | "browser";

export interface SpeechControls {
  speaking: boolean;
  wordIndex: number;
  /**
   * Progress through the currently-highlighted word, 0..1. Updated on
   * every RAF tick while audio plays. Consumers can use it to drive a
   * sub-word "fill sweep" effect (Yoto-style) — a gradient background
   * that advances across the word's letters in real time. At 0 the
   * word has just activated; at 1 the next word is about to activate.
   *
   * When not speaking (or between words), this is 0.
   */
  wordProgress: number;
  words: string[];
  speak: (text: string, onEnd?: () => void) => void;
  stop: () => void;
  /** Pause the currently-playing audio WITHOUT resetting. Safe to call
   *  when nothing is playing (no-op). Companion to resume(). Used by the
   *  Word Glow modal to quiet the narrator while the child reads a
   *  definition; narration picks up mid-word when the modal dismisses. */
  pause: () => void;
  /** Resume playback after pause(). Safe to call when nothing is paused
   *  (no-op) or when stop() has been called (also no-op — doesn't start
   *  anything fresh). */
  resume: () => void;
  prefetch: (text: string, storyId?: string, pageIdx?: number) => void;
  setStoryContext: (storyId?: string, pageIdx?: number) => void;
  // Voice mode
  voiceMode: VoiceMode;
  setVoiceMode: (mode: VoiceMode) => void;
  // AI voice settings
  aiVoice: AIVoiceName;
  setAiVoice: (v: AIVoiceName) => void;
  aiSpeed: number;
  setAiSpeed: (s: number) => void;
  // Browser voice settings (kept for fallback)
  voice: SpeechSynthesisVoice | null;
  setVoice: (v: SpeechSynthesisVoice) => void;
  rate: number;
  setRate: (r: number) => void;
  allVoices: SpeechSynthesisVoice[];
  // Loading state for AI audio
  loading: boolean;
}
